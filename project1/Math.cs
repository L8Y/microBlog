namespace project1
{
    public class Math
    {
        public int Add(int num1, int num2)
        {
            int result = num1 + num2;
            return result;
        }

        public int Sub(int num1, int num2)
        {
            int result = num1 - num2;
            return result;
        }

        public int Multiple(int num1, int num2)
        {
            int result = num1 * num2;
            return result;
        }
    }
}